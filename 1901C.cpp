int f(int a,int b){
    if(a>b)swap(a,b);
    int dx = b-a;

    int ans;
    if(dx%4==3){
        ans = dx/2+1;
    }else{
        ans = dx/2;
    }
    return ans+a;
}

inline void solve(){
    int n;cin>>n;
    vector<int> a(n+1,0);for(int i=1;i<=n;++i)cin>>a[i];
    int mx = *max_element(a.begin()+1,a.end());
    int mi = *min_element(a.begin()+1,a.end());
    int dx = mx-mi;

    int x;
    bitset<32> b(dx);
    for(int i=31;i>=0;--i){
        if(b[i]==1){
            x = i;
            break;
        }
    }
    
    if(dx==0){
        cout<<0<<endl;
        return;
    }
    int ans = x+1;
    cout<<ans<<endl;
    if(ans>n)return;
    for(int i=1;i<=ans;++i){
        int y = f(mi,mx);
        cout<<y<<" ";
        mi = (mi+y)/2;
        mx = (mx+y)/2;
    }
    cout<<endl;

	return;
}


