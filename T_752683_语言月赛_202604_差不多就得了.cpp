template<class T>
ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

#define d(x) cerr<<"#---"<<#x<<"=="<<x<<endl;
#define cerr if(0)cerr
#define int long long

inline void solve(){
    int n;cin>>n;
    vector<pii> a(n+1);
    for(int i=1;i<=n;++i){
        cin>>a[i].first;
        a[i].second = i;
    }

    sort(a.begin()+1,a.end());

    vector<ll> pre(n+1,0);
    for(int i=1;i<=n;++i){
        pre[i] = pre[i-1]+a[i].first;
    }

    ll s = pre[n];
    int limit = 0;
    for(int i=1;i<=n;++i){
        if(abs(s-(pre[i]*2))<=100){
            limit = i;
        }
    }

    

    vector<int> w(n+1,0);
    for(int i=1;i<=n;++i){
        if(i<=limit){
            w[ a[i].second ] = 1;
        }else{
            break;
        }
    }

    for(int i=1;i<=n;++i){
        cerr<<a[i].first<<","<<a[i].second<<" ";
    }cerr<<endl;
    d(w);
    d(limit);

    for(int i=1;i<=n;++i){
        if(w[i]){
            cout<<'A';
        }else{
            cout<<'B';
        }
    }

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















