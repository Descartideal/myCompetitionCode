const ll INF = 1e9;

inline ll f(vector<int> a,int n,int mx){
    vector<int> dp(n+1,0);
    dp[0] = -INF;

    for(int i=1;i<=n;++i){
        if(a[i]>mx){
            a[i]=-INF;
        }
        dp[i] = max(a[i],dp[i-1]+a[i]);
    }

    return (*max_element(dp.begin(),dp.end())-mx);
}

inline void solve(){
    int n;cin>>n;
    vector<int> a(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }
    ll mx = *max_element(a.begin(),a.end());
    //cerr<<mx<<endl;

    ll ans = 0;
    for(int i=0;i<=mx;++i){
        ans = max(ans,f(a,n,i));
    }

    cout<<ans<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);

		solve();
	
	return 0;
}


















