inline void solve(){
    int p,f,cnts,cntw,s,w;cin>>p>>f>>cnts>>cntw>>s>>w;
    if(p>f)swap(p,f);
    if(s>w){
        swap(s,w);
        swap(cnts,cntw);
    }
    //cerr<<s<<" "<<w<<" "<<cnts<<" "<<cntw<<endl;

    ll cnt = cnts+cntw;

    
    auto check = [&](int x){
        ll x_start = x;
        ll m = 0;
        int cs = min(x,cnts);
        int cw = 0;
        x -= cs;
        if(x==0){
            ;
        }else{
            cw = min(x,cntw);
        }

        x -= cw;
        if(x>0){
            return false;
        }

        m = cs*s+cw*w;
        if(m>p+f){
            return false;
        }

        if(m<=p+f-2*w){
            return true;
        }

        int remain = p+f-m;
        for(int i=0;i<=cs;++i){
            if(i*s>f){
                //cerr<<i<<" "<<f<<" "<<x_start<<endl;
                break;
            }
            int r = f-i*s;//cerr<<r<<endl;
            int j = min((r/w),cw);
            
            r -= j*w;
            //cerr<<f<<" "<<x_start<<" "<<r<<" "<<i<<" "<<j<<endl;
            if(r<=remain){
                return true;
            }
        }

        return false;
    };

    //cerr<<check(16)<<endl;
    //cerr<<"ads"<<endl;

    int l=0,r=p+f,mid=(l+r)>>1;
    while(l<r){//cerr<<7;
        if(check(mid)){
            l = mid+1;
        }else{
            r = mid;
        }
        mid=(l+r)>>1;
    }
    if(!check(mid)){
        mid--;
    }
    cout<<mid<<'\n';

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















