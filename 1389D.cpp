#define int long long

inline void solve(){
    int n,k;cin>>n>>k;
    int l1,r1;cin>>l1>>r1;
    int l2,r2;cin>>l2>>r2;

    int seg0,seg1,seg2,ihave;
    ihave = max(0LL,min(r1,r2)-max(l1,l2));
    seg0 = max(0LL,max(l1,l2)-min(r1,r2));
    seg1 = max(r1,r2)-min(l1,l2)-ihave;

    int total = k-ihave*n;
    if(total<=0){
        cout<<0<<endl;
        return;
    }

    if(seg0>=seg1){
        if(total<=seg1){
            cout<<total+seg0<<endl;
        }else{
            total-=seg1;
            cout<<2*total+seg0+seg1<<endl;
        }
        return;
    }

    int res = 0;

    int w = seg0+seg1, v =seg1;
    int cyc = min(total/v,n);
    total -= cyc*v;
    res += cyc*w;
    
    if(cyc==n){
        res += 2*total;
    }else{
        if(cyc>=1){
            int res1 = total+seg0;
            int res2 = 2*total;

            res += min(res1,res2);
        }else{
            res += total+seg0;
        }
    }
    cout<<res<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















