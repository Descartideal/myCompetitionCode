template<class T>
ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

int dx[4] = {0,1,0,-1};
int dy[4] = {-1,0,1,0};

inline void solve(){
    int h,w;cin>>h>>w;
    vector<vector<int>> a(h+1,vector<int>(w+1,0));
    for(int i=1;i<=h;++i){
        for(int j=1;j<=w;++j){
            char x;cin>>x;
            if(x=='.'){
                a[i][j] = 0;
            }else{
                a[i][j] = -1;
            }
        }
    }

    int cnt = 0;
    auto bfs = [&](int y,int x){
        a[y][x] = cnt;
        queue<pii> q;
        q.push({y,x});

        bool res = 1;
        while(!q.empty()){//cerr<<7;
            auto [hh,ww] = q.front();q.pop();
            for(int k=0;k<4;++k){
                if(hh+dy[k]<1 || hh+dy[k]>h || ww+dx[k]<1 || ww+dx[k]>w){
                    res = 0;
                    continue;
                }

                if(a[hh+dy[k]][ww+dx[k]]==0){
                    q.push({hh+dy[k],ww+dx[k]});
                    a[hh+dy[k]][ww+dx[k]]=cnt;
                }
            }
        }

        return res;
    };

    

    for(int i=1;i<=h;++i){
        for(int j=1;j<=w;++j){
            if(a[i][j]==0){
                cnt++;
                bool judg = bfs(i,j);
                if(!judg){
                    cnt--;
                }
            }
        }
    }
/** 
    for(int i=1;i<=h;++i){
        cerr<<a[i]<<endl;
    }
*/
    cout<<cnt<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















