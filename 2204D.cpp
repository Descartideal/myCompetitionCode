template<class T>
ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline void solve(){
    int n,m;cin>>n>>m;
    vector<vector<int>> g(n+1);
    for(int i=1;i<=m;++i){
        int u,v;cin>>u>>v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    vector<int> b(n+1,0);
    function<void(int,int)> dfs1;
    dfs1 = [&](int x,int color){
        b[x] = color;
        for(int y:g[x]){
            if(b[y]==0)dfs1(y,color);
        }
        return;
    };

    

    int cnt = 0;
    
    for(int i=1;i<=n;++i){
        if(b[i]!=0)continue;
        cnt++;
        dfs1(i,cnt);
    }
    //cerr<<cnt<<" "<<b<<endl;

    vector<int> c(n+1,-1);

    int cnt0,cnt1,down;
    vector<int> w(cnt+1,-1);
    function<void(int,int)> dfs2;
    dfs2 = [&](int x,int color){
        c[x] = color;
        if(color==1){
            cnt1++;
        }else{
            cnt0++;
        }

        for(int y:g[x]){
            if(c[y]==-1){
                dfs2(y,1-color);
            }else{
                if(1-color==c[y]){
                    continue;
                }else{
                    down = 1;
                    return;
                }
            }
        }

        return;
    };

    

    int ans = 0;
    for(int i=1;i<=n;++i){
        if(w[ b[i] ]!=-1)continue;
        w[b[i]]=1;
        cnt0=0;cnt1=0;down=0;
        dfs2(i,0);
        if(down==1){
            cnt0=0;
            cnt1=0;
        }
        ans += max(cnt0,cnt1);
    }
    cout<<ans<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















