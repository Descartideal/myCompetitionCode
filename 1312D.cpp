using b32 = bitset<32>;
using b64 = bitset<64>;

const ll M = 998244353;

ll powm(ll a,ll b){
    ll x=a;x%=M;
    ll res = 1;
    while(b){
        if(b&1){
            res*=x;res%=M;
        }
        b>>=1;
        x=x*x;x%=M;
    }

    return res;
}

ll inv(ll x){
    return powm(x,M-2);
}

ll power(ll x){
    ll res = 1;
    for(;x;x--){
        res*=x;res%=M;
    }
    return res;
}

ll mul(ll x,ll y){
    return x*y%M;
}

inline void solve(){
    ll n,m;cin>>n>>m;
    if(n==2 || m<n-1){
        cout<<0<<endl;
        return;
    }

    ll t1 = mul( mul(power(m),n-2) , powm(2,n-3) );
    ll t2 = mul( power(n-1) , power(m-n+1) );

    ll res = mul(t1,inv(t2));
    cout<<res<<endl;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















