#define d(x) cerr<<"#---"<<#x<<"=="<<x<<endl;
#define cerr if(0)cerr

#define int long long

const ll INF = LONG_LONG_MAX;

inline void solve(){
    int n,q;cin>>n>>q;
    d(n);d(q);
    vector<int> op(n+1,0),xi(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>op[i]>>xi[i];
    }
    vector<int> qu(q+1,0);for(int i=1;i<=q;++i)cin>>qu[i];
    d(qu);

    vector<i128> c(n+1,0);
    for(int i=1;i<=n;++i){
        if(op[i]==1){
            c[i] = c[i-1]+1;
        }else{
            c[i] = c[i-1]*(xi[i]+1);
        }

        if(c[i]>INF)c[i]=INF;
    }

    d(c);d(xi);

    function<ll(ll)> f;
    f = [&](int x){
        d(x);
        ll l=1,r=n,mid=(l+r)>>1;
        while(l<r){
            //d(l);d(r);d(mid);
            if(c[mid]<x){
                l = mid+1;
            }
            if(c[mid]>=x){
                r = mid;
            }
            
            mid=(l+r)>>1;
        }
        int pos = mid;
        d(pos);
        if(op[pos]==1){
            cerr<<"as\n";
            return xi[pos];
        }

        return f( (x-1)%((ll)c[pos-1])+1 );
    };

    for(int i=1;i<=q;++i){
        cout<<f(qu[i])<<" ";
    }cout<<endl;

    cerr<<endl<<endl<<endl;
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















