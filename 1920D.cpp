ostream& operator<<(ostream& os, __int128 x) {
    if(x==0)return os<<'0';
    if(x<0){
        os<<'-';
        x=-x;
    }
    string s;
    while(x){
        s += char('0' + x%10);
        x /= 10;
    }
    reverse(s.begin(), s.end());
    return os<<s;
}


