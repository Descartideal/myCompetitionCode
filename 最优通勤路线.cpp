const ll INF = 4e18;

int dijkstra(int n,int s,int t,const vector<vector<pii>>& g){

    vector<ll> dist(n+1,INF);

    priority_queue<pii,
    vector<pii>,
    greater<pii>
    > pq;

    dist[s] = 0;
    pq.push({0,s});

    while(!pq.empty()){
        auto [du,u] = pq.top();pq.pop();

        if(du!=dist[u])continue;
        if(u==t)return dist[u];

        for(auto [v,w]:g[u]){
            if(dist[v]>dist[u]+w){
                dist[v] = dist[u]+w;
                pq.push({dist[v],v});
            }
        }
    }

    return INF;
}

inline void solve(){ 
    int n,m;cin>>n>>m;
    vector<vector<pii>> g(n+1);
    for(int i=1;i<=m;++i){
        int u,v,w;cin>>u>>v>>w;
        g[u].push_back({v,w});
        g[v].push_back({u,w});
    }
    int s,x,t;cin>>s>>x>>t;

    
    int s1 = dijkstra(n,s,x,g);
    int s2 = dijkstra(n,x,t,g);
    cout<<s1+s2<<endl;

	return;
}


