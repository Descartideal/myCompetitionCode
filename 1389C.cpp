inline void solve(){
    string s;cin>>s;
    int n = s.size();

    vector<int> a(10,0);
    for(char c:s){
        a[ c-'0' ]++;
    }
    sort(a.begin(),a.end());
    int ans1 = n-a[9];
    

    vector<int> b(100,0);
    for(int i=0;i<=99;++i){
        int x = i/10,y=i%10;
        if(x==y)continue;
        char xc=to_string(x)[0],yc=to_string(y)[0];
        //cerr<<xc<<" "<<yc<<endl;
        
        int state = 0;
        for(int j=0;j<n;++j){
            if(state==0){
                //cerr<<s[j]<<" "<<xc<<endl;
                if(s[j]==xc){
                    state=1;
                }
            }else{
                if(s[j]==yc){//cerr<<"ads ";
                    state=0;
                    b[i]++;
                }
            }
        }
    }

    //cout<<b[4]<<endl;
    //cout<<max_element(b.begin(),b.end())-b.begin()<<endl;
    int ans2 = (n - (*max_element(b.begin(),b.end())) *2);

    //if(ans1<ans2)cout<<"a: ";
    cout<<min(ans1,ans2)<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















