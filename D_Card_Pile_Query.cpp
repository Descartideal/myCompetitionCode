inline void solve(){
    int n,m;cin>>n>>m;
    vector<int> dui(n+1,0);
    iota(dui.begin(),dui.end(),0);
    vector<int> paiDown(n+1,-1);
    vector<int> paiUp(n+1,0);

    for(int i=1;i<=m;++i){
        int u,v;cin>>u>>v;
        if(paiDown[u]==-1){
            dui[u] = 0;
            paiDown[u] = v;
        }else{
            int t = paiDown[u];
            paiUp[t] = 0;
            paiDown[u] = v;
        }
        paiUp[v] = u;
    }

    for(int i=1;i<=n;++i){
        int cnt = 0;
        int t = dui[i];
        while(t>=1){
            cnt++;
            t = paiUp[t];
        }
        cout<<cnt<<" ";
    }cout<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















