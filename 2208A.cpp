ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline void solve(){
    int n;cin>>n;
    unordered_map<int,int> t;
    bool d = 1;
    for(int i=0;i<n;++i){
        for(int j=0;j<n;++j){
            int x;cin>>x;
            t[x]++;
            if(t[x]>n*(n-1)){
                d = 0;
            }
        }
    }

    if(d){
        cout<<"YES"<<endl;
    }else{
        cout<<"NO"<<endl;
    }

	return;
}


