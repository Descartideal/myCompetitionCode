#define int long long


ll lowbit(ll x){
    return x & -x;
}
void change(int pos,int da,int n,vector<int>& re){
    for(;pos<=n;pos+=lowbit(pos)){
        re[pos] += da;
    }
}
ll sum_prefix(int pos,vector<int>& re){
    ll ans = 0;
    for(;pos;pos-=lowbit(pos)){
        ans+=re[pos];
    }
    return ans;
}


void solve(){
    ll n,h,k;cin>>n>>h>>k;
    ll s = 0;
    vector<int> a(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>a[i];
        s+=a[i];
    }


    ll remain = h%s;
    if(remain>0){
        ll ss = 0;
        int range = 0;
        for(int i=1;i<=n;++i){
            ss+=a[i];
            if(ss>remain){
                range = i;
                break;
            }
        }



        ll mx = 0;
        for(int i=range+1;i<=n;++i){
            mx = max(mx,(ll)a[i]);
        }
        vector<int> b(range+1,0);
        for(int i=1;i<=range;++i){
            change(i,a[i],range+1,b);
        }

        int ans = range;
        for(int i=1;i<=range;++i){
            change(i,mx-a[i],range+1,b);
            int l=1,r=range,mid=((l+r)>>1);
            while(l<r){
                mid = ((l+r)>>1);
                ll t = sum_prefix(mid,b);
                if(t<remain){
                    l = mid+1;
                }else if(t>remain){
                    r = mid;
                }else if(t==remain){
                    l=mid;r=mid;
                }
            }
            mid = ((l+r)>>1);
            change(i,a[i]-mx,range+1,b);
            ans = min(ans,mid);
        }


        mx = a[range];
        b.clear();b=vector<int>(range+1,0);for(int i=1;i<=range;++i){change(i,a[i],range+1,b);}
        for(int i=1;i<=range;++i){
            change(i,mx-a[i],range+1,b);change(range,a[i]-mx,range+1,b);
            int l=1,r=range,mid=((l+r)>>1);
            while(l<r){
                mid = ((l+r)>>1);
                ll t = sum_prefix(mid,b);
                if(t<remain){
                    l = mid+1;
                }else if(t>remain){
                    r = mid;
                }else if(t==remain){
                    l=mid;r=mid;
                }
            }
            mid = ((l+r)>>1);
            change(i,a[i]-mx,range+1,b);change(range,mx-a[i],range+1,b);
            ans = min(ans,mid);
        }

        ll cnt = h/s;
        cout<<cnt*(n+k)+ans<<'\n';
    }else{
        ll cnt = h/s;
        cout<<cnt*n+(cnt-1)*k<<'\n';
    }

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















