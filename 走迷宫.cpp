const ll INF = 4e18;

int dijkstra(int n,int s,int t,const vector<vector<pii>>& g){

    vector<ll> dist(n+1,INF);

    priority_queue<pii,
    vector<pii>,
    greater<pii>
    > pq;

    dist[s] = 0;
    pq.push({0,s});

    while(!pq.empty()){
        auto [du,u] = pq.top();pq.pop();

        if(du!=dist[u])continue;
        if(u==t)return dist[u];

        for(auto [v,w]:g[u]){
            if(dist[v]>dist[u]+w){
                dist[v] = dist[u]+w;
                pq.push({dist[v],v});
            }
        }
    }

    return INF;
}

int dx[9]={0,-1,1,0,0,0,0,0,0};
int dy[9]={0,0,0,-1,1,0,0,0,0};
int dz[9]={0,0,0,0,0,-1,1,0,0};
int dt[9]={0,0,0,0,0,0,0,-1,1};

inline void solve(){
    int s,total;
    int n;cin>>n;
    int a[n+1][n+1][n+1][n+1];
    vector<vector<pii>> g(n*n*n*n+1);
    for(int i=1;i<=n*n;++i){
        for(int j=1;j<=n*n;++j){
            int dex = (i-1)*n*n+j;

            int x,y,z,t;
            x=(i-1)%n+1;
            y=(j-1)%n+1;
            z=(j-1)/n+1;
            t=(i-1)/n+1;

            dex = (t-1)*n*n*n+(z-1)*n*n+(x-1)*n+y;



            char c;cin>>c;
            
            if(c=='S')s=dex;

            if(c=='E'){
                total=dex;
                
            }

            if(c=='#'){
                a[x][y][z][t] = 1;
            }else{
                if(c=='S')s=dex;
                if(c=='E')total=dex;
                a[x][y][z][t] = 0;
            }
        }
    }

    for(int i=1;i<=n*n;++i){
        for(int j=1;j<=n*n;++j){
            int dex = (i-1)*n*n+j;

            int x,y,z,t;
            x=(i-1)%n+1;
            y=(j-1)%n+1;
            z=(j-1)/n+1;
            t=(i-1)/n+1;

            if(a[x][y][z][t]==1)continue;

            dex = (t-1)*n*n*n+(z-1)*n*n+(x-1)*n+y;

            for(int k=1;k<=8;++k){
                int nx=x+dx[k],ny=y+dy[k],nz=z+dz[k],nt=t+dt[k];
                if(nx<1 || ny<1 || nz<1 || nt<1 || nx>n || ny>n || nz>n || nt>n){
                    continue;
                }
                if(a[nx][ny][nz][nt])continue;
                int ndex = (nt-1)*n*n*n+(nz-1)*n*n+(nx-1)*n+ny;
                g[dex].push_back({ndex,1});
            }
        }
    }


    int ans = dijkstra(n*n*n*n,s,total,g);
    if(ans==INF){
        cout<<-1<<endl;
    }else{
        cout<<ans<<endl;
    }

	return;
}


