#define d(x) cerr<<"#---"<<#x<<"=="<<x<<endl;
#define cerr if(0)cerr

inline void solve(){
    int n;cin>>n;
    vector<int> a(n+1);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }

    vector<int> b = a;
    sort(b.begin()+1,b.end());
    int mid = n/2+1;
    int midNumber = b[mid];

    for(int i=1;i<=n;++i){
        if(a[i]<midNumber){
            a[i] = -1;
        }else if(a[i]>midNumber){
            a[i] = 1;
        }else{
            a[i] = 0;
        }
    }

    vector<int> pref1(n+1,0);
    vector<int> pre1(n+1,0);
    vector<int> pre0(n+1,0);

    for(int i=1;i<=n;++i){
        pre0[i] += pre0[i-1]+(a[i]==0);
        pre1[i] += pre1[i-1]+(a[i]==1);
        pref1[i] += pref1[i-1]+(a[i]==-1);
    }

    d(pre0);
    d(pre1);
    d(pref1);

    vector<int> dp(n+1,0);
    vector<vector<int>> g(n+1);
    for(int len=1;len<=n;len+=2){
        for(int i=1;i+len-1<=n;++i){
            int j = i+len-1;
            if( abs(( pre1[j]-pre1[i-1] )-( pref1[j]-pref1[i-1] )) < (pre0[j]-pre0[i-1]) ){
                g[i-1].push_back(j);
            }
        }
    }

    d(g);

    for(int i=0;i<=n;++i){
        if(i!=0 && dp[i]==0)continue;
        for(auto x:g[i]){
            dp[x] = max(dp[i]+1,dp[x]);
        }
    }
    d(dp);

    cout<<dp[n]<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















