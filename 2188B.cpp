#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n;cin>>n;
    string s;cin>>s;
    s = "10" + s + "01";
    n+=4;
    int cnt = 0,state = 0;
    int res = 0;
    for(int i=0;i<n;++i){
        char c = s[i];
        if(state==0 && c=='0'){
            state=1;
            continue;
        }
        if(state==1 && c=='0'){
            cnt++;
            continue;
        }
        if(state==1 && c=='1'){
            res++;
            cnt--;
            state=0;
            res += ((cnt+2)/3);
            cnt=0;
            continue;
        }
        if(state==0 && c=='1'){
            res++;
            continue;
        }
    }


    cout<<(res-2)<<endl;
	return;
}


int main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















