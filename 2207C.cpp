#define int long long

inline void solve(){
    ll n,h;cin>>n>>h;
    vector<ll> a(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }

    vector<vector<int>> pm(n+1,vector<int>(n+1,0));
    for(int i=1;i<=n;++i){
        pm[i][i] = i;
        for(int j=i+1;j<=n;++j){
            if(a[pm[i][j-1]] < a[j]){
                pm[i][j] = j;
            }else{
                pm[i][j] = pm[i][j-1];
            }
            pm[j][i] = pm[i][j];
        }
    }

    vector<vector<int>> pre(n+1,vector<int>(n+2,0));
    vector<vector<int>> suf(n+1,vector<int>(n+2,0));
    for(int i=1;i<=n;++i){
        for(int j=1;j<=n;++j){
            pre[i][j] = (h-a[pm[i][j]]) + pre[i][j-1];
        }
    }
    for(int i=1;i<=n;++i){
        for(int j=n;j>=1;--j){
            suf[i][j] = (h-a[pm[i][j]]) + suf[i][j+1];
        }
    }

    int ans = 0;
    for(int i=1;i<=n;++i){
        for(int j=i;j<=n;++j){
            int m = pm[i][j];
            ans = max(ans ,  pre[i][m]+suf[j][m+1]  );
        }
    }
    cout<<ans<<endl;
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















