template<class T>
ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline void solve(){
    int n,l,r;cin>>n>>l>>r;
    string s;cin>>s;
    unordered_map<char,int> t;

    ll ans = 0;
    for(int i=0;i<n;++i){
        char c = s[i];
        int jl=i-r-1,jr=i-l;
        //cerr<<c<<" "<<jl<<" "<<jr<<endl;
        if(jl>=0){
            char h=s[jl];
            t[h]--;
            //cerr<<"--"<<h<<endl;
        }
        if(jr>=0){
            char h=s[jr];
            t[h]++;
            //cerr<<"++"<<h<<endl;
        }
        ans+=t[c];
    }
    cout<<ans<<endl;
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);

		solve();
	
	return 0;
}


















