inline void solve(){
    int n,k;cin>>n>>k;
    string s;cin>>s;
    vector<int> a(n+1);
    int cnt = 0;
    for(int i=1;i<=n;++i){
        if(s[i-1]=='B')cnt++;
        a[i] = cnt;
    }
    if(cnt==k){
        cout<<0<<endl;
        return;
    }
    if(cnt<k){
        for(int i=1;i<=n;++i){
            if(s[i-1]=='A'){
                cnt++;
            }
            if(cnt==k){
                cout<<1<<endl;
                cout<<i<<" B\n";
                return;
            }
        }
    }

    for(int i=1;i<=n;++i){
        if(s[i-1]=='B'){
            cnt--;
        }
        if(cnt==k){
            cout<<1<<endl;
            cout<<i<<" A\n";
            return;
        }
    }


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















