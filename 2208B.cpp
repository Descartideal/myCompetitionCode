inline ll gs(vector<int> v,int cnt){
    if(cnt<=0)return 0;
    sort(v.begin(),v.end(),greater<int>());
    ll s=0;
    for(int i=0;i<cnt && i<(int)v.size();++i){
        s+=v[i];
    }
    return s;
}

inline void solve(){
    int n,k,p,m;cin>>n>>k>>p>>m;
    vector<int> a(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }

    ll first = 0;ll cycle = 0;
    if(p<=k){
        first = a[p];
    }else{
        vector<int> pre;
        ll sum = 0;
        for(int i=1;i<p;++i){
            pre.push_back(a[i]);
            sum += a[i];
        }
        first = a[p] + (sum - gs(pre,k-1));
    }

    if(first > m){
        cout<<0<<endl;
        return;
    }
    vector<int> other;
    ll sum = 0;
    for(int i=1;i<=n;++i){
        if(i==p)continue;
        other.push_back(a[i]);
        sum += a[i];
    }
    cycle = a[p] + (sum - gs(other,k-1));

    ll ans = 1 + (m-first)/cycle;
    cout<<ans<<endl;
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















