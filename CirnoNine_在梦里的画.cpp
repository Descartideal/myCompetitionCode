istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

int di[4]={-1,0,1,0},dj[4]={0,1,0,-1};

inline void solve(){
    int n,m;cin>>n>>m;
    vector<string> A(n+1);
    for(int i=1;i<=n;++i){
        cin>>A[i];
        A[i]=" "+A[i];
    }

    vector<vector<int>> pre1(n+1,vector<int>(m+1,0));
    vector<vector<int>> pre2(n+1,vector<int>(m+1,0));
    for(int i=1;i<=n;++i){
        for(int j=1;j<=m;++j){
            pre1[i][j]=pre1[i][j-1]+(A[i][j]=='1');
        }
    }
    for(int j=1;j<=m;++j){
        for(int i=1;i<=n;++i){
            pre2[i][j]=pre2[i-1][j]+pre1[i][j];
        }
    }

    auto p = [&](int x,int y){
        if(x<0 || y<0)return 0;
        return pre2[x][y];
    };

    auto sum = [&](int x,int y,int hen,int shu){
        return p(x,y) -p(x-hen,y) -p(x,y-shu) +p(x-hen,y-shu) ;    
    };

    
    auto check = [&](int x){
        if(x==0)return true;
        //cerr<<"x:"<<x<<endl;
        int squ = x*x;
        vector<vector<int>> a(n+1,vector<int>(m+1,0));
        int cnt=0;
        int si,sj;
        for(int i=1;i<=n;++i){
            for(int j=1;j<=m;++j){
                a[i][j] = sum(i,j,x,x);
                if(a[i][j]==squ){
                    cnt++;
                    si=i,sj=j;
                }
            }
        }
        if(x>=1 && cnt==0){
            //cerr<<"mei you\n";
            return false;
        }

        queue<pii> q;
        q.push({si,sj});//cerr<<"s:"<<si<<" "<<sj<<endl;
        int cntVis = 0;
        vector<vector<int>> vis(n+1,vector<int>(m+1,0)),full(n+1,vector<int>(m+1,0)),diff(n+2,vector<int>(m+2,0)),cover(n+1,vector<int>(m+1,0));

        auto ful = [&](int i,int j,int x){//cerr<<"asdf "<<i<<" "<<j<<" "<<x<<endl;
            int x1 = i - x + 1;
            int y1 = j - x + 1;
            int x2 = i;
            int y2 = j;

            diff[x1][y1] ++;
            diff[x2 + 1][y1] --;
            diff[x1][y2 + 1] --;
            diff[x2 + 1][y2 + 1] ++;
        };

        while(!q.empty()){
            auto [i,j]=q.front();q.pop();//cerr<<i<<" "<<j<<endl;
            cntVis++;
            ful(i,j,x);
            vis[i][j]=1;
            for(int k=0;k<4;++k){
                int ni=i+di[k],nj=j+dj[k];
                if(ni<=0 || nj<=0 || ni>n || nj>m)continue;
                if(vis[ni][nj])continue;
                if(a[ni][nj]==squ){
                    vis[ni][nj]=1;
                    q.push({ni,nj});
                }
            }
        }

        for(int i=1;i<=n;++i){
            for(int j=1;j<=m;++j){
                cover[i][j] = diff[i][j]+cover[i-1][j]+cover[i][j-1]-cover[i-1][j-1];
            }
        }

        //cerr<<cover<<endl;;

        for(int i=1;i<=n;++i){
            for(int j=1;j<=m;++j){
                if(cover[i][j]>0){
                    cover[i][j]=1;
                }else{
                    cover[i][j]=0;
                }
            }
        }


        if(cntVis<cnt){
            //cerr<<"wu lian tong\n";
            return false;
        }

        //cerr<<full<<endl;

        for(int i=1;i<=n;++i){
            for(int j=1;j<=m;++j){
                if(cover[i][j]!=(int)(A[i][j]-'0')){
                    //cerr<<"bu dui\n";
                    return false;
                }
            }
        }

        return true;
    };

    

    int l=0,r=min(n,m),mid=(l+r)>>1;
    while(l<r){
        if(check(mid)){
            l=mid+1;
        }else{
            r=mid;
        }
        mid=(l+r)>>1;
    }
    
    if(!check(mid)){
        mid--;
    }
    if(mid<0)mid=0;

    cout<<mid<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















