#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n;cin>>n;
    vector<ll>a(n),b(n);
    for(auto&x:a)cin>>x;
    for(auto&x:b)cin>>x;

    sort(a.begin(),a.end());

    vector<ll>pref(n+1,0);
    for(int i=1;i<=n;i++){
        pref[i]=pref[i-1]+b[i-1];
    }

    ll ans=0;
    for(int k=1;k<=n;k++){
        if(pref[k]>n)break;
        ll need=pref[k];
        ll x=a[n-need];
        ans=max(ans,x*k);
    }
    cout<<ans<<"\n";
}


int main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















