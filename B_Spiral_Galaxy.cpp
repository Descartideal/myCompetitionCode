inline void solve(){
    int n,m;cin>>n>>m;
    vector<vector<int>> a(n+1,vector<int>(m+1,0));
    for(int i=1;i<=n;++i){
        for(int j=1;j<=m;++j){
            char c;cin>>c;
            if(c=='#'){
                a[i][j] = 1;
            }
        }
    }

    auto judg = [&](int h1,int w1,int h2,int w2){
        for(int i=h1;i<=h2;++i){
            for(int j=w1;j<=w2;++j){
                if(a[i][j]!=a[h1+h2-i][w1+w2-j]){
                    return false;
                }
            }
        }
        return true;
    };

    int cnt = 0;
    for(int h1=1;h1<=n;++h1){
        for(int w1=1;w1<=m;++w1){
            for(int h2=h1;h2<=n;++h2){
                for(int w2=w1;w2<=m;++w2){
                    if(judg(h1,w1,h2,w2)){
                        cnt++;
                    }
                }
            }
        }
    }
    cout<<cnt<<endl;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















