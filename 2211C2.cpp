struct DSU{
    vector<int> f;
    DSU(int n):f(n+1){iota(f.begin(),f.end(),0);}
    int find(int x){return f[x]==x?x:f[x]=find(f[x]);}
    void merge(int x,int y){f[find(x)]=find(y);}
};

inline void solve(){
    int n,k;cin>>n>>k;
    vector<int> a(n+1,0),b(n+1,0);
    for(int i=1;i<=n;++i)cin>>a[i];
    for(int i=1;i<=n;++i)cin>>b[i];

    DSU dsu(n);

    for(int i=1;i+k<=n;++i){
        if(a[i]==a[i+k]){
            dsu.merge(i,i+k);
        }else{
            if(b[i]==-1)b[i]=a[i];
            if(b[i+k]==-1)b[i+k]=a[i+k];
            if(b[i]!=a[i] || b[i+k]!=a[i+k]){
                cout<<"NO\n";
                return;
            }
        }
    }

    vector<int> val(n+1,-1);
    for(int i=1;i<=n;++i){
        if(b[i]==-1)continue;
        int f = dsu.find(i);
        if(val[f]==-1)val[f]=b[i];
        else if(val[f]!=b[i]){
            cout<<"NO\n";
            return;
        }
    }

    vector<int> cnt(n+1);
    for(int i=1;i<=k;++i){
        cnt[a[i]]++;
        int x = val[dsu.find(i)];
        if(x!=-1)cnt[x]--;
    }

    for(int i=1;i<=n;++i){
        if(cnt[i]<0){
            cout<<"NO\n";
            return;
        }
    }
    cout<<"YES\n";
 
    return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















