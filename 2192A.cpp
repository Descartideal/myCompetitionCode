using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n;cin>>n;
    string s;cin>>s;

    int ans = 1;
    for(int i=0;i<n;++i){
        int cnt = 0;
        char stata = 0;
        string t = s.substr(i,n-i)+s.substr(0,i);
        for(int i=0;i<n;++i){
            char c = t[i];
            if(stata!=c){
                cnt++;
                stata = c;
            }
        }

        ans = max(ans,cnt);
    }
    cout<<ans<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















