inline void solve(){
    int n;cin>>n;
    string s;cin>>s;

    string st;
    st.reserve(n);
    for (char ch : s) {
        if (!st.empty() && st.back()==ch){
            st.pop_back();
        }else{
            st.push_back(ch);
        }
    }


    if(st.empty()){
        cout<<"YES"<<endl;
    }else{
        cout<<"NO"<<endl;
    }

    return;
}


