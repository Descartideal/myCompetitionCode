inline void solve(){
    int n;cin>>n;
    
    vector<int> a(n);for(int& x:a)cin>>x;

    if(n==1){
        cout<<(a[0]==1 ? 1 : (a[0]+1)/2+1)<<endl;
        return;
    }

    sort(a.begin(),a.end());
    ll s = accumulate(a.begin(),a.end(),0LL);
    vector<ll> pre(n);
    pre[0] = a[0];
    for(int i=1;i<n;++i){
        pre[i] = pre[i-1]+a[i];
    }
    int l=0,r=n-1;
    for(int i=0;i<n-1;++i){
        if(pre[i]*2>=s){
            l = i;
            break;
        }else{
            l = i;
        }
    }

    ll ans = (s+1)/2 + (n-l-1);
    if(pre[l]*2>s && pre[l]!=(s+1)/2){
        ans++;
    }

    cout<<ans<<endl;
    return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















