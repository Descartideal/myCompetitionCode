struct TwoSat {
    int n;
    vector<vector<int>> e;
    vector<bool> ans;
    TwoSat(int n) : n(n), e(2 * n), ans(n) {}
    void add(int u, bool f, int v, bool g) {
        e[2 * u + !f].push_back(2 * v + g);
        e[2 * v + !g].push_back(2 * u + f);
    }
    bool work() {
        vector<int> id(2 * n, -1), dfn(2 * n, -1), low(2 * n, -1);
        vector<int> stk;
        int now = 0, cnt = 0;
        auto tarjan = [&](auto self, int u) -> void {
            stk.push_back(u);
            dfn[u] = low[u] = now++;
            for (auto v : e[u]) {
                if (dfn[v] == -1) {
                    self(self, v);
                    low[u] = min(low[u], low[v]);
                } else if (id[v] == -1) {
                    low[u] = min(low[u], dfn[v]);
                }
            }
            if (dfn[u] == low[u]) {
                int v;
                do {
                    v = stk.back();
                    stk.pop_back();
                    id[v] = cnt;
                } while (v != u);
                ++cnt;
            }
        };
        for (int i = 0; i < 2 * n; ++i) {
            if (dfn[i] == -1) {
                tarjan(tarjan, i);
            }
        }
        for (int i = 0; i < n; ++i) {
            if (id[2 * i] == id[2 * i + 1]) return false;
            ans[i] = id[2 * i] > id[2 * i + 1];
        }
        return true;
    }
    vector<bool> answer() {
        return ans;
    }
};



inline void solve(){
    string s;cin>>s;
    int n = s.size();
    vector<int> a(n,0);
    for(int i=0;i<n;++i){
        if(s[i]=='*'){
            a[i]=-1;
            continue;
        }
        int x = s[i]-'0';
        if(i>=1){
            if(s[i-1]=='*'){
                x--;
            }
        }
        if(i<n-1){
            if(s[i+1]=='*'){
                x--;
            }
        }

        a[i] = x;
    }

    cerr<<a<<endl;

    TwoSat b(n);
    vector<int> pre(n,-1);
    for(int i=0;i<n;++i){cerr<<i<<endl;
        if(a[i]==-1)continue;
        if(i==0){
            if(a[i]==0){
                pre[i+1]=0;
                pre[i]=0;
            }
            if(a[i]==2){
                pre[i+1]=1;
                pre[i]=1;
            }

            if(a[i]==1){
                b.add(i,0,i+1,1);
            }
            continue;
        }
        if(i==n-1){
            if(a[i]==0){
                pre[i-1]=0;
                pre[i]=0;
            }
            if(a[i]==2){
                pre[i-1]=1;
                pre[i]=1;
            }

            if(a[i]==1){
                b.add(i,0,i-1,1);
            }
            continue;
        }

        if(a[i]==0){
            pre[i-1]=0;
            pre[i]=0;
            pre[i+1]=0;
            continue;
        }
        if(a[i]==3){
            pre[i-1]=1;
            pre[i]=1;
            pre[i+1]=1;
            continue;
        }
        if(a[i]==1){
            b.add(i-1,1,i,1);

            b.add(i,1,i-1,1);

            b.add(i+1,1,i-1,1);
        }//cerr<<i<<"-end\n";

        if(a[i]==2){
            b.add(i-1,0,i,0);

            b.add(i,0,i-1,0);

            b.add(i+1,0,i-1,0);
        }

        
    }

    b.work();
    cout<<b.ans<<endl;
    cout<<pre<<endl<<endl;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















