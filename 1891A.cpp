inline void solve(){
    int n;cin>>n;
    vector<int> a(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }

    if( is_sorted(a.begin()+1,a.end()) ){
        cout<<"YES"<<endl;
        return;
    }


    for(int i=1;;++i){
        int l = (1<<i)+1 , r = min( (1<<(i+1)) , n );
        if( !is_sorted( a.begin()+l , a.begin()+r+1 ) ){
            cout<<"NO"<<endl;
            return;
        }

        if( (1<<(i+1))>=n ){
            break;
        }
    }

    cout<<"YES"<<endl;
	return;

}


