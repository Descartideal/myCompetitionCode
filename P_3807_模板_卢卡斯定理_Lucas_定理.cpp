inline ll mpow(ll a,ll b,ll m){
    a%=m;ll res=1;
    while(b){
        if(b&1){
            res*=a;res%=m;
        }
        a=a*a;a%=m;
        b>>=1;
    }
    return res;
}

inline ll inv(ll x,ll p){
    return mpow(x,p-2,p);
}

inline ll mfact(ll x,ll p){
    ll res=1;
    while(x){
        res*=x;res%=p;
        x--;
    }
    return res;
}

inline ll C(ll n,ll m,ll p){
    if(m>n)return 0;
    return mfact(n,p)*inv(mfact(n-m,p)*mfact(m,p)%p,p);
}

inline ll Lucas(ll n,ll m,ll p){
    if(m==0)return 1;
    return C(n%p,m%p,p)*Lucas(n/p,m/p,p)%p;
}


inline void solve(){
    int n,m,p;cin>>n>>m>>p;
    n = n+m;
    cout<<Lucas(n,m,p)<<endl;
	return;
}


