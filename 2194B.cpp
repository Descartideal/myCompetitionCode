void solve(){
    int n,x,y;cin>>n>>x>>y;
    vector<int> a(n);for(int& x:a){cin>>x;}

    vector<int> b(n),c(n),d(n);
    for(int i=0;i<n;++i){
        b[i] = a[i]%x;
        c[i] = a[i]/x;
        d[i] = b[i]+c[i]*(x-y);
    }
    int mx = max_element(d.begin(),d.end())-d.begin();

    i64 sum = a[mx];

    for(int i=0;i<n;++i){
        if(i==mx)continue;
        sum+=c[i]*y;
    }
    cout<<sum<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















