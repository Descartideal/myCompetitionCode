using b32 = bitset<32>;
using b64 = bitset<64>;

inline void solve(){
    ll n,q;cin>>n>>q;
    vector<ll> a(n),b(q);
    for(ll& x:a)cin>>x;for(ll& x:b)cin>>x;
    vector<ll> c;int mi = 60;
    for(int i=0;i<q;++i){
        if(b[i]<mi){
            c.push_back(b[i]);
            mi = b[i];
        }
    }

    for(ll& x:a){
        for(ll& y:c){
            if(x%(1<<y)==0){
                x += (1<<(y-1));
            }
        }
    }

    for(ll x:a){
        cout<<x<<" ";
    }cout<<endl;

	return;
}


