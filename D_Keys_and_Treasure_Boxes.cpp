#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n,m;cin>>n>>m;
    vector<int> a(n+1),b(m+1);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }
    for(int i=1;i<=m;++i){
        cin>>b[i];
    }

    sort(a.begin()+1,a.end());
    sort(b.begin()+1,b.end());
    int x=1,y=1,cnt = 0;
    while (x<=n && y<=m) {
        if(a[x]<=b[y]){
            cnt++;
            x++;
            y++;
        }else{
            y++;
        }
        
    }
    cout<<cnt<<endl;


	return;
}


int main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















