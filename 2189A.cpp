#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n,h,l;cin>>n>>h>>l;
    vector<int> a(n);for(int& x:a)cin>>x;
    int cnt1=0,cnt2=0;
    for(int& x:a){
        if(x<=l && x<=h){
            cnt2++;
            continue;
        }
        if(x<=l || x<=h){
            cnt1++;
            continue;
        }
    }

    if(cnt1>=cnt2){
        cout<<cnt2<<endl;
    }else{
        cout<<(cnt1+(cnt2-cnt1)/2)<<endl;
    }

	return;
}


int main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















