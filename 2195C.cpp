inline void solve(){
    int n;cin>>n;
    vector<int> a(n);for(int&x:a)cin>>x;
    vector<int> w(n+1,0);

    int cnt = 0;
    for(int i=0;i<=n-2;++i){
        if(  (a[i]==a[i+1])  ||  ((7-a[i])==a[i+1])  ){
            if(w[i]==0){
                cnt++;
                w[i]=cnt;
                w[i+1]=cnt;
            }else{
                w[i+1]=w[i-1];
            }
        }
    }

    vector<int> b;
    int stata = 0;
    cnt = 0;

    for(int i=0;i<n+1;++i){
        if(stata==0 && w[i]==0){
            continue;
        }
        if(stata==0 && w[i]>=1){
            stata=w[i];
            cnt++;
        }else if(stata==w[i]){
            cnt++;
        }else if(stata!=w[i] && w[i]>=1){
            b.push_back(cnt);
            stata=w[i];
            cnt=1;
        }else if(stata!=w[i] && w[i]==0){
            b.push_back(cnt);
            stata=w[i];
            cnt=0;
        }else{
            cnt=0;
        }
    }

    int ans = 0;
    for(int x:b){
        ans += (x/2);
    }
    cout<<ans<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















