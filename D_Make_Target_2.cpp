inline ll fangxing(ll x){
    x>>=1;
    return 2*x*x+3*x+1;
}

inline ll juxing(ll x,ll y){
    if(x<0 || y<0){
        return 0;
    }
    if(x<y)swap(x,y);
    ll res=fangxing(y);
    ll d=x-y,z=0;
    if(x&1){
        if(d&1){
            z=(d>>1);
        }else{
            z=((d>>1));
        }
    }else{
        if(d&1){
            z=((d>>1)+1);
        }else{
            z=d>>1;
        }
    }
    //cerr<<endl<<d<<" "<<z<<" "<<y+1<<" "<<res<<endl;
    res+=(y+1)*z;
    return res;
}

inline ll qubianjuxing(ll x,ll y){
    if(x<0 || y<0){
        return 0;
    }
    if(x<y)swap(x,y);
    return juxing(x,y)-juxing(x,0)-juxing(y,0)+1;
}

inline ll yiban(ll l,ll r,ll d,ll u){
    if(l<0 && r<0){
        l=-l;r=-r;
    }
    if(d<0 && u<0){
        d=-d;u=-u;
    }

    ll res = 0;
    if((l^r)<0 && (u^d)<0){
        l=-l;d=-d;
        res+=juxing(r,u);
        res+=qubianjuxing(l,u);
        res+=qubianjuxing(l,d);
        res+=qubianjuxing(r,d);
        res+=(juxing(l,0)+juxing(d,0)-2);
        return res;
    }

    if((l^r)<0 || (u^d)<0){
        if(d<0){
            swap(d,l);
            swap(u,r);
        }
        res+=juxing(r,u);
        res-=juxing(r,d-1);
        res+=juxing(-l,u);
        res-=juxing(-l,d-1);

        res-=juxing(0,u);
        res+=juxing(0,d-1);

        return res;
    }

    res+=juxing(r,u);
    res-=juxing(l-1,u);
    res-=juxing(r,d-1);
    res+=juxing(l-1,d-1);

    return res;
}

