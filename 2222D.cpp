#define d(x) cerr<<"#---"<<#x<<"=="<<x<<endl;
#define int long long
#define cerr if(0)cerr

inline void solve(){
    int n;cin>>n;
    vector<ll> a(n+1);for(int i=1;i<=n;++i)cin>>a[i];

    d(a);

    vector<ll> pre(n+1,0);for(int i=1;i<=n;++i)pre[i]=pre[i-1]+a[i];

    for(int i=n;i>=1;--i){
        pre[i] = pre[i-1];
    }

    d(pre);

    vector<pair<ll,ll>> b(n+1);
    for(int i=1;i<=n;++i){
        b[i].first = pre[i];
        b[i].second = i;
    }
    sort(b.begin()+1,b.end());

    vector<int> ans(n+1,0);
    for(int i=1;i<=n;++i){
        ans[ b[i].second ] = n-i+1;
    }
    
    for(int i=1;i<=n;++i){
        cout<<ans[i]<<" ";
    }cout<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















