const int INF = 1e9+7;

inline void solve(){
    int n,k;cin>>n>>k;
    vector<int> dp(n+1,INF);
    vector<int> c(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>c[i];
    }
    vector<int> p(k+1,0);
    for(int i=1;i<=k;++i){
        cin>>p[i];
        dp[ p[i] ] = 0;
    }

    vector<vector<int>> g(n+1);
    for(int i=1;i<=n;++i){
        int cnt;cin>>cnt;
        if(cnt==0)continue;
        for(int j=1;j<=cnt;++j){
            int x;cin>>x;
            g[i].push_back(x);
        }
    }

    function<void(int)> dfs;
    dfs = [&](int x){
        int res = 0;
        for(int y:g[x]){
            if(dp[y]==INF)dfs(y);
            res += dp[y];
        }
        if(g[x].empty()){
            res = INF;
        }

        dp[x] = min(c[x],res);
    };


    for(int i=1;i<=n;++i){
        if(dp[i]==INF)dfs(i);
    }
    
    for(int i=1;i<=n;++i){
        cout<<dp[i]<<" ";
    }cout<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















