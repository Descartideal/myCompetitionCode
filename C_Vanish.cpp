#define int long long

inline void solve(){
    int n,k;cin>>n>>k;
    vector<int> a(n+1);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }

    unordered_map<int,int> t;
    for(int i=1;i<=n;++i){
        t[ a[i] ]++;
    }

    vector<ll> b;
    int cnt = 0;
    for(auto [u,v]:t){
        if(v==0)continue;
        b.push_back(u*v);
        cnt++;
    }
    

    sort(b.begin(),b.end());
    //cerr<<b<<endl;
    int d = max(cnt-k,0LL);

    ll res = accumulate(b.begin(), ( b.begin() + d ), 0LL);
    cout<<res<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















