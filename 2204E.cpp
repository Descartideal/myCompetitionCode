ll st(ll x){
    int res = 0;
    while(x){
        res+= x%10;
        x/=10;
    }
    return res;
}

bool check(vector<int> a,int x){
    int sum1 = 0;
    for(int i=0;i<10;++i){
        sum1 += i*a[i];
    }

    string s = "";
    int xx = x;
    while(1){
        if(xx!=0)s += to_string(xx);
        if(xx<=9){
            break;
        }else{
            xx = st(xx);
        }
    }
    //cerr<<x<<" "<<s<<endl;
    for(int i=0;i<s.size();++i){
        char c = s[i];
        int x = c-'0';
        a[x]--;
        if(a[x]<0){
            return false;
        }
    }

    int sum = 0;
    for(int i=0;i<10;++i){
        sum += i*a[i];
    }

    if(sum==x){
        //cerr<<x<<endl;
        return true;
    }else{
        return false;
    }
}

inline void solve(){
    string s;cin>>s;
    if(s.size()==1){
        cout<<s<<endl;
    }
    //cerr<<"a:"<<s<<endl;
    vector<int> a(10,0);
    for(int i=0;i<s.size();++i){
        char c = s[i];
        int x = c-'0';
        a[x]++;
    }
    //cerr<<a<<endl;

    int sum = 0;
    for(int i=0;i<10;++i){
        sum += i*a[i];
    }
    //cerr<<"sum: "<<sum<<endl;
    for(int i=sum;i>=0;--i){
        if(check(a,i)){//cerr<<"asdf"<<endl;
            int x = i;
            string s = "";
            while(1){
                s += to_string(x);
                if(x<=9){
                    break;
                }else{
                    x = st(x);
                }
            }
            for(int i=0;i<s.size();++i){
                char c = s[i];
                int x = c-'0';
                a[x]--;
            }
            string t = "";
            for(int i=9;i>=0;--i){
                while(a[i]--){
                    t += to_string(i);
                }
            }
            s = t+s;
            cout<<s<<endl;
            return;
        }
    }

	return;
}


