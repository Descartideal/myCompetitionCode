using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    i64 a,b;cin>>a>>b;
 
    i64 mi=b,mx=b;

    int cnt = 0;
    while (mx<=2*a+1) {
        if(mi<=a && a<=mx){
            cout<<cnt<<endl;
            return;
        }else{
            mi = 2*mi-1;
            mx = 2*mx+1;
            ++cnt;
        }
    }

    cout<<-1<<endl;
	return;
}


int main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















