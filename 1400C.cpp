template<class T>
ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline void solve(){string s;cin>>s;
    int x;cin>>x;
    
    int n = s.size();
    s = ' '+s;

    vector<int> w(n+1,-1);
    vector<pii> t;
    for(int i=1;i<=min(x,n-x);++i){
        int c = s[i]-'0';
        if(c){
            w[i+x]=1;
        }else{
            w[i+x]=0;
        }
    }
    for(int i=x+1;i<=n-x;++i){
        int c = s[i]-'0';
        if(c){
            t.push_back({i-x,i+x});
        }else{
            if(w[i-x]==1 || w[i+x]==1){
                cout<<-1<<'\n';
                return;
            }
            w[i-x] = w[i+x] = 0;
        } 
    }
    for(int i=max(n-x+1,x+1);i<=n;++i){
        int c = s[i]-'0';
        if(c){
            if(w[i-x]==0){
                cout<<-1<<'\n';
                return;
            }
            w[i-x]=1;
        }else{
            if(w[i-x]==1){
                cout<<-1<<'\n';
                return;
            }
            w[i-x]=0;
        }
    }

    for(int i=1;i<=n;++i){
        int c = s[i]-'0';
        if(c){
            if(i+x>n && i-x<1){
                cout<<-1<<endl;
                return;
            }
        }
    }

    for(auto [l,r]:t){
        if(w[l]==1 || w[r]==1)continue;
        if(w[l]==-1 || w[r]==-1)continue;
        cout<<-1<<'\n';
        return;
    }
    string ans = "";
    //cerr<<n<<endl;
    for(int i=1;i<=n;++i){
        if(w[i]==-1)w[i]=1;//cerr<<'a';

        ans = ans + ((char)(w[i]+'0'));
    }

    cout<<ans<<'\n';
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















