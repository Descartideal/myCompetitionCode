ll mpow(int a,int b,int m){
    int res = 1;
    while(b){
        (res*=a)%=m;
        (a=a*a)%=m;
        b>>=1;
    }
    return res;
}

inline void solve(){
    int n,p,k;cin>>n>>p>>k;
    vector<int> a(n+1);for(int i=1;i<=n;++i)cin>>a[i];
    vector<int> b(n+1);
    for(int i=1;i<=n;++i){
        b[i] = (((mpow(a[i],3,p)-k)*a[i])%p+p)%p;
    }

    ll ans = 0;
    unordered_map<int,int> t;
    for(int i=1;i<=n;++i){
        t[ b[i] ]++;
    }
    for(auto [x,y]:t){
        if(y==0)continue;
        ans += (y-1)*y/2;
    }
    cout<<ans<<'\n';

	return;
}


