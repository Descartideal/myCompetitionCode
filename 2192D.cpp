#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n;cin>>n;
    vector<vector<int>> g(n+1);
    vector<int> b(n+1);
    for(int i=1;i<=n;++i){
        cin>>b[i];
    }
    for(int i=1;i<=n-1;++i){
        int u,v;cin>>u>>v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    vector<int> siz(n+1),dep(n+1),sum(n+1),tf(n+1),fa(n+1);

    function<int(int,int,int)> dfs1;
    dfs1 = [&](int x,int f,int deep){
        fa[x] = f;dep[x] = deep;
        int ans = 1;
        int res = b[x];
        int tfd = 0;
        for(int y:g[x]){
            if(y==f)continue;
            ans+=dfs1(y,x,deep+1);
            res+=sum[y];
            tfd+=tf[y]+sum[y];
        }
        siz[x] = ans;
        sum[x] = res;
        tf[x] = tfd;
        return siz[x];
    };
    dfs1(1,0,1);

    

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















