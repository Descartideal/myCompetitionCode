#define int long long

inline void solve(){
    int n;cin>>n;
    vector<ll> f(n+1);
    for(int i=1;i<=n;++i){
        cin>>f[i];
    }

    ll s = (f[1]+f[n])/(n-1);
    vector<ll> a(n+1);
    a[1] = (f[2]+f[n])-(n-2)*s; a[1]/=2;
    a[n] = (f[1]+f[n-1])-(n-2)*s; a[n]/=2;

    vector<ll> prea(n+1);
    prea[1] = a[1];
    vector<ll> cnts(n+1);
    cnts[1] = a[1]*4;

    for(int i=2;i<=n-1;++i){
        ll t = (f[i+1]+f[n])-s*(n-i-1);
        t -= cnts[i-1];
        a[i] = t/2;
        cnts[i] = a[i]*4+cnts[i-1]+2*prea[i-1];
        prea[i] = prea[i-1]+a[i];
    }

    //cerr<<prea<<endl<<cnts<<endl<<endl;


    for(int i=1;i<=n;++i){
        cout<<a[i]<<" ";
    }cout<<"\n";
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















