inline void solve(){
    int n;cin>>n;
    vector<int> a(n);cin>>a;
    sort(a.begin(),a.end());
    int cnt0 = 0;
    for(int x:a){
        if(x==0){
            cnt0++;
        }else{
            break;
        }
    }
    if(cnt0>=2){
        cout<<"Alice"<<endl;
        return;
    }
    if(cnt0==1){
        for(int i=1;i<n;++i){
            if(a[i]==i)continue;
            if(a[i]==i-1){
                cout<<"Bob"<<endl;
                return;
            }
            cout<<"Alice"<<endl;
            return;
        }
        cout<<"Bob"<<endl;
        return;
    }
    for(int i=0;i<n;++i){
        if(a[i]==i+1)continue;
        if(a[i]==i){
            cout<<"Bob"<<endl;
            return;
        }
        cout<<"Alice"<<endl;
        return;
    }
    cout<<"Bob"<<endl;
    return;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















