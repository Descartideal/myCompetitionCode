istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

struct DSU{
    vector<int> f;
    DSU(int n=0){init(n);}
    void init(int n){
        f.resize(n+1);
        iota(f.begin(),f.end(),0);
    }
    int find(int x){
        return x==f[x]?x:f[x]=find(f[x]);
    }
    bool merge(int x,int y){
        x=find(x);y=find(y);
        if(x==y)return false;
        f[x]=y;
        return true;
    }
};

struct edge{
    int u,v,w;
    bool operator<(const edge& other)const{
        return w < other.w;
    }
};

inline void solve(){
    int n=readint();
    vector<int> a(n+1);
    for(int i=1;i<=n;++i){
        a[i]=readint();
    }
    vector<vector<int>> b(n+1,vector<int>(n+1));
    for(int i=1;i<=n;++i){
        for(int j=1;j<=n;++j){
            b[i][j]=readint();
        }
    }
    vector<edge> es;
    es.resize(n*(n+1)/2);
    for(int i=1;i<=n;++i){
        for(int j=i;j<=n;++j){
            es.push_back({i,j,b[i][j]});
        }
    }
    sort(es.begin(),es.end());
    vector<int> isfull(n+1,0);

    DSU dsu(n);

    ll ans = 0;
    for(auto &e:es){
        int u=e.u , v=e.v , w=e.w;
        if(u==v){
            if (!isfull[u]) {
                ans += 1LL * (a[u] - 1) * w;
                isfull[u] = 1;
            }
        }else{
            if (dsu.find(u) == dsu.find(v)) continue;
            ll cu = isfull[u] ? 1LL : 1LL * a[u];
            ll cv = isfull[v] ? 1LL : 1LL * a[v];

            ans += (cu + cv - 1) * 1LL * w;

            isfull[u] = isfull[v] = 1;
            dsu.merge(u, v);
        }
    }

    cout<<ans<<'\n';
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t=readint();
	while(t-->0){
		solve();
	}
	return 0;
}


















