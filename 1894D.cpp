using b32 = bitset<32>;
using b64 = bitset<64>;

inline void solve(){
    int n,m;cin>>n>>m;
    vector<int>a(n),b(m);for(int&i:a)cin>>i;for(int&i:b)cin>>i;

    sort(b.begin(),b.end(),greater<int>());
    vector<int>ans;ans.reserve(n+m);
    int p=0;

    for(int i=0;i<n;i++){
        while(p<m&&b[p]>a[i]){
            ans.push_back(b[p++]);
        }
        ans.push_back(a[i]);
    }

    while(p<m){
        ans.push_back(b[p++]);
    }

    for(int i=0;i<n+m;i++){
        cout<<ans[i]<<(i+1==n+m?'\n':' ');
    }
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















