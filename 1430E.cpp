struct BIT {
    int n;vector<ll> tr;
    BIT(int n = 0) { init(n); }

    void init(int n_) {
        n = n_;
        tr.assign(n + 1, 0);
    }

    int lowbit(int x) {
        return x & -x;
    }

    void add(int x, ll v) {
        for (int i = x; i <= n; i += lowbit(i)) {
            tr[i] += v;
        }
    }

    ll sum(int x) {
        ll res = 0;
        for (int i = x; i > 0; i -= lowbit(i)) {
            res += tr[i];
        }
        return res;
    }

    ll query(int l, int r) {
        if (l > r) return 0;
        return sum(r) - sum(l - 1);
    }
};

