inline void solve(){
    string s;cin>>s;
    int n = s.size();
    int cnt1=0,cnt2=0,cnt3=0;
    for(int i=0;i<n;++i){
        if(s[i]=='1'){
            cnt1++;
        }else if(s[i]=='2'){
            cnt2++;
        }else{
            cnt3++;
        }
    }
    if(cnt1==0 || cnt2==0 || cnt3==0){
        cout<<0<<endl;
        return;
    }

    vector<int> a,b;
    for(int i=0;i<n-1;++i){
        if(s[i]!=s[i+1]){
            a.push_back((s[i]-'0')^(s[i+1]-'0'));
            b.push_back(i);
        }
    }
    int ans = 1e7;
    for(int i=0;i<a.size()-1;++i){
        if(a[i]!=a[i+1]){
            cnt1=0;cnt2=0;cnt3=0;
            int beg=b[i],ed=b[i+1]+1;
            for(int i=beg;i<=ed;++i){
                if(s[i]=='1'){
                    cnt1++;
                }else if(s[i]=='2'){
                    cnt2++;
                }else{
                    cnt3++;
                }
                if(cnt1==0 || cnt2==0 || cnt3==0){
                    continue;
                }else{
                    ans = min( ans , i-beg+1 );
                }
            }
        }
    }

    cout<<ans<<endl;

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















