#ifdef __unix__
#define gc getchar_unlocked
#else
#define gc _getchar_nolock
#endif

inline int readint(){
    int k=0,f=1,c=gc();
    for(;!isdigit(c);c=gc())if(c=='-')f=-1;
    for(;isdigit(c);c=gc())k=10*k+(c&15);
    return k*f;
}


const int N = 4000;

inline void solve(){
    vector<bool> isPrime(N+1,1);
    vector<int> p;

    for(int i=2;i<=N;++i){
        if(isPrime[i]){
            p.push_back(i);
        }

        for(int j=0;j<p.size()&&i*p[j]<=N;++j){
            isPrime[i*p[j]]=0;
            if(i%p[j]==0)break;
        }
    }cerr<<p.size()<<endl;

    int n=readint();
    vector<int> a(n+1);
    vector<pii> ans(1,{0,0});

    for(int i=1;i<=n;++i){
        int x = readint();int z = x;
        

        vector<int> d;
        unordered_map<int,int> t;

        for(int y:p){
            if(x%y==0){
                d.push_back(y);//cerr<<y<<endl;
                t[y]++;
                x/=y;
            }
            while(x%y==0){
                x/=y;t[y]++;
            }

            if(y*y>x){
                break;
            }
        }
        if(x!=1){
            d.push_back(x);
            t[x]++;
        }

        if(d.size()>1){
            int g = pow(d[0],t[ d[0] ]);
            int h = z/g;
            if(h!=1){
                ans.push_back({g,h});
            }else{
                ans.push_back({-1,-1});
            }
        }else{
            ans.push_back({-1,-1});
        }

        //cerr<<d<<endl;
    }
    

    for(int i=1;i<=n;++i){
        cout<<ans[i].first<<" ";
    }cout<<endl;
   
    for(int i=1;i<=n;++i){
        cout<<ans[i].second<<" ";
    }cout<<endl;





	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















