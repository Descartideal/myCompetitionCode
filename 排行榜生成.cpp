struct stu{
    int id,sol,time;
};

inline void solve(){ 
    int n,m;cin>>n>>m;
    vector<stu> a(n+1);
    for(int i=1;i<=n;++i){
        cin>>a[i].id;
        int sol=0,time=0;
        for(int i=1;i<=m;++i){
            int x;cin>>x;
            if(x==0){
                ;
            }else{
                sol++;
                time += x;
            }
            
        }
        a[i].sol=sol;
        a[i].time=time;
    }
    sort(a.begin()+1,a.end(),[&](auto A,auto B){
        if(A.sol!=B.sol)return A.sol>B.sol;
        if(A.time!=B.time)return A.time<B.time;
        return A.id<B.id;
    });

    int cnt = 1;
    for(int i=1;i<=n;++i){
        if(a[i-1].sol==a[i].sol && a[i-1].time==a[i].time){
            ;
        }else{
            cnt = i;
        }

        cout<<cnt;
        cout<<" "<<a[i].id<<" "<<a[i].sol<<" "<<a[i].time<<endl;
    }


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















