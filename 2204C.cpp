ll mgcd(ll a,ll b){
    if(a>b)swap(a,b);
    if(a==0)return b;
    return mgcd(b%a,a);
}

inline void solve(){
    ll a,b,c,m;cin>>a>>b>>c>>m;

    ll A,B,C,AB,AC,BC,ABC;

    ll t = (a*b)/mgcd(a,b);

    ABC = (  m / ( t*c/(mgcd(t,c)) )  );

    AB = (m/ ((a*b)/mgcd(a,b)) )-ABC;
    AC = (m/ ((a*c)/mgcd(a,c)) )-ABC;
    BC = (m/ ((b*c)/mgcd(b,c)) )-ABC;

    A = (m/a) - AB - AC - ABC;
    B = (m/b) - AB - BC - ABC;
    C = (m/c) - AC - BC - ABC;

    cout<<2*ABC+3*AB+3*AC+6*A<<" "<<2*ABC+3*AB+3*BC+6*B<<" "<<2*ABC+3*AC+3*BC+6*C<<endl;

	return;
}


