inline void solve(){
    int n;cin>>n;
    vector<int> a(n+1);
    map<int,int> b;
    for(int i=1;i<=n;++i){
        cin>>a[i];
        b[ a[i] ]++;
    }

    if(n<=3){
        cout<<-1<<endl;
        return;
    }

    vector<int> c;
    unordered_map<int,int> d;
    for(auto [x,y]:b){
        c.push_back(y);
        if(y>=2){
            d[x] = 1;
        }
    }
    sort(c.begin(),c.end());
    if(c.size()==1){
        cout<<-1<<endl;
        return;
    }
    unordered_map<int,int> w;
    vector<int> ans;
    if(c.back()>=2 && c[c.size()-2]>=2){
        int s = 2;
        for(int i=1;i<=n;++i){
            int x = a[i];
            if(d[x]==1){
                if(w[x]==0){
                    ans.push_back(1);
                    w[x] = s;
                    s = 5-s;
                }else{
                    ans.push_back(w[x]);
                }
            }else{
                ans.push_back(1);
            }
        }

        for(int x:ans){
            cout<<x<<" ";
        }cout<<endl;
        return;
    }
    cout<<-1<<endl;
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















