inline void solve(){
    int n;cin>>n;
    string s1,s2;cin>>s1>>s2;
    vector<int> a1(n+1,0),a2(n+1,0);
    for(int i=1;i<=n;++i){
        char c1=s1[i-1],c2=s2[i-1];
        if(c1=='('){
            a1[i] = 1;
        }else{
            a1[i] = -1;
        }
        if(c2=='('){
            a2[i] = 1;
        }else{
            a2[i] = -1;
        }
    }

    vector<int> a(n+1,0);
    int cnt0 = 0;
    vector<int> preCnt0(n+1,0);
    for(int i=1;i<=n;++i){
        preCnt0[i] = preCnt0[i-1];
        if(a1[i]==a2[i]){
            if(a1[i]==1){
                a[i] = 1;
            }else{
                a[i] = -1;
            }
        }else{
            a[i] = 0;
            cnt0++;
            preCnt0[i]++;
        }
    }
    if(cnt0 & 1){
        cout<<"NO"<<endl;
        return;
    }

    vector<int> pre(n+1,0);for(int i=1;i<=n;++i)pre[i]=pre[i-1]+a[i];
    //d(pre)
    for(int i=1;i<=n;++i){
        if(pre[i]<0){
            cout<<"NO"<<endl;
            return;
        }
    }

    if(pre[n]!=0){
        cout<<"NO"<<endl;
        return;
    }
    for(int i=1;i<=n;++i){
        if( (preCnt0[i]&1) && pre[i]==0 ){
            cout<<"NO"<<endl;
            return;
        }
    }
    cout<<"YES"<<endl;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















