inline void solve(){
    ll n,x,y;cin>>n>>x>>y;
    if(x<y){
        cout<<"NO"<<endl;
        return;
    }

    if(n==1){
        if(x==y){
            cout<<"YES"<<endl;
            return;
        }else{
            cout<<"NO"<<endl;
            return;
        }
    }

    if(x*y<=0){
        cout<<"YES"<<endl;
        return;
    }

    if(x<0){
        x = -x;
        y = -y;
        if(x<y)swap(x,y);
    }

    ll mx = y*n;
    if(x>=mx){
        cout<<"YES"<<endl;
        return;
    }
    cout<<"NO"<<endl;

	return;
}


