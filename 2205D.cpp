using b32 = bitset<32>;
using b64 = bitset<64>;

inline void solve(){
    int n;cin>>n;
    vector<int>a(n);
    for(int&x:a)cin>>x;

    vector<int>L(n,-1),R(n,-1),P(n,-1),stk;
    stk.reserve(n);
    for(int i=0;i<n;++i){
        int lst=-1;
        while(!stk.empty() && a[stk.back()]<a[i]){
            lst=stk.back();
            stk.pop_back();
        }

        if(!stk.empty()){
            P[i]=stk.back();
            R[stk.back()]=i;
        }
        if(lst!=-1){
            P[lst]=i;
            L[i]=lst;
        }
        stk.push_back(i);
    }

    int rt=stk[0];
    while(P[rt]!=-1)rt=P[rt];

    vector<int>dep(n,0),stk2;
    dep[rt]=1;
    int h=1;
    stk2.push_back(rt);
    while(!stk2.empty()){
        int u=stk2.back();
        stk2.pop_back();
        int d=dep[u];

        if(L[u]!=-1){
            dep[L[u]]=d+1;
            h=max(h,dep[L[u]]);
            stk2.push_back(L[u]);
        }
        if(R[u]!=-1){
            dep[R[u]]=d+1;
            h=max(h,dep[R[u]]);
            stk2.push_back(R[u]);
        }
    }
    cout<<(n-h)<<endl;
}
	



signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















