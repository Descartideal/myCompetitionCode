#include <bits/stdc++.h>
using namespace std;

#define int long long

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;

int gcd(int x,int y){
    if(x>y)return gcd(y,x);
    y %= x;
    if(y==0)return x;
    return gcd(y,x);
}

ll lcm(ll x,ll y){
    return x*y/gcd(x,y);
}

void solve(){
    int n,m;cin>>n>>m;
    int s = n+m;
    vector<bool> a(s+1,0);
    vector<bool> j(s+1,0);
    int slcm,jlcm=0;

    for(int i=1;i<=n;++i){
        int x;cin>>x;
        if(i==1)slcm=x;
        if(!jlcm)slcm=lcm(x,slcm);
        if(slcm>s)jlcm=1;
        
        if(a[x] || j[x]){
            a[x] = 1;
            continue;
        }else{
            a[x] = 1;
            int mul = 1;
            while(x*mul<=s){
                j[x*mul]=1;
                mul++;
            }
        }
    }
    
    int cnta=0,cntb=0,cntc=0;
    for(int i=1;i<=m;++i){
        int x;cin>>x;
        if(!j[x])cntb++;
        else if(!jlcm && x%slcm==0)cnta++;
        else cntc++;
    }

    //cerr<<cnta<<" "<<cntb<<" "<<cntc<<endl;
    if(cntc&1)cnta++;
    
    if(cnta>cntb){
        cout<<"Alice"<<endl;
    }else{
        cout<<"Bob"<<endl;
    }
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















