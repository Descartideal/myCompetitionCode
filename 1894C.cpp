#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;

using b32 = bitset<32>;
using b64 = bitset<64>;

inline void solve(){
    int n,k;cin>>n>>k;
    vector<int> a(n+1);
    for(int i=1;i<=n;++i){
        cin>>a[i];
    }
    vector<vector<int>> g(n+1);
    for(int i=1;i<=n;++i){
        int x = a[i];
        if(x>n){
            continue;
        }
        g[n-i+1].push_back( (n-i+x+n)%n+1 );
    }

    vector<int> dep(n+1,0);
    queue<pii> q;q.push({1,1});
    while(!q.empty()){
        auto [x,d] = q.front();q.pop();
        dep[x] = d;
        for(int y:g[x]){
            if(dep[y]!=0){
                cout<<"Yes"<<endl;
                return;
            }
            dep[y] = d+1;
            q.push({y,d+1});
        }
    }
    int mx = *max_element(dep.begin(),dep.end());
    if(k>mx-1){
        cout<<"No"<<endl;
    }else{
        cout<<"Yes"<<endl;
    }

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















