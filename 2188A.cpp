#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll =long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n;cin>>n;
    int a=1,b=n;
    vector<int> c(n);
    for(int i=n-1;i>=0;--i){
        if((n-i)&1){
            c[i] = a;
            ++a;
        }else{
            c[i] = b;
            --b;
        }
    }
    for(int i=0;i<n;++i){
        cout<<c[i]<<" ";
    }cout<<endl;

	return;
}


int main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















