istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline void solve(){
    int h,m;cin>>h>>m;
    if(h>m)swap(h,m);
    if(h>=0 && h<=23 && m>=0 && m<=59){
        if(h<=9){
            cout<<0<<h;
        }else{
            cout<<h;
        }
        cout<<':';
        if(m<=9){
            cout<<0<<m;
        }else{
            cout<<m;
        }
        cout<<'\n';

    }else{
        cout<<-1<<endl;
    }


	return;
}


