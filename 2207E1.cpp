int lowbit(int x){
    return x & -x;
}

void change(int pos,int da,int n,vector<int>& re){
    for(;pos<=n;pos+=lowbit(pos)){
        re[pos] += da;
    }
}

int sum_prefix(int pos,vector<int>& re){
    int ans = 0;
    for(;pos;pos-=lowbit(pos)){
        ans+=re[pos];
    }
    return ans;
}

int mmex(int pos,vector<int>& re){
    return sum_prefix(pos+1,re);
}

void addm(int pos,int n,vector<int>& re){
    change(pos+1,-1,n,re);
    return;
}

const int INF = 9999999;
 

inline void solve(){
    int n;cin>>n;
    vector<int> a(n+1);
    vector<bool> w(n+1,0);
    for(int i=1;i<=n;++i){
        cin>>a[i];
        w[ a[i] ]  =  1;
    }
    for(int i=1;i<=n;++i){
        if(a[i]>n){
            cout<<"NO"<<endl;
            return;
        }
    }
    for(int i=1;i<n;++i){
        if(a[i]<a[i+1]){
            cout<<"NO"<<endl;
            return;
        }
    }

    vector<int> b(n+1,0);
    for(int i=1;i<=n;++i){
        b[i] = a[i]-(n-i);
    }
    
    
    vector<int> t;
    for(int i=0;i<=n;++i){
        if(w[i]==0){
            t.push_back(i);
        }
    }

    //cerr<<t<<endl;

    int cnt = 0;
    vector<int> ans;

    vector<int> re(n+2,0);
    for(int i=1;i<=n+1;++i){
        if(w[i-1])change(i,1,n+1,re);
    }

    /** 
    cerr<<a<<endl;
    cerr<<b<<endl;
    cerr<<"ys:";
    for(int i=1;i<=n+1;++i){
        cerr<<sum_prefix(i,re)<<" ";
    }cerr<<endl;
*/
    
    for(int i=n-1;i>=1;--i){//cerr<<"asdf:"<<ans<<endl;
        if(mmex(a[i],re)>b[i]){
            ans.push_back(t[cnt]);
            addm(a[i],n+1,re);
            cnt++;
        }else{
            ans.push_back(INF);
        }
    }

    if(mmex(a[n],re)>b[n]){
        ans.push_back(t[cnt]);
        addm(a[n],n+1,re);
        cnt++;
    }else{
        ans.push_back(INF);
    }


    cout<<"YES"<<endl;
    for(int i=n-1;i>=0;--i){
        cout<<ans[i]<<" ";
    }cout<<endl;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















