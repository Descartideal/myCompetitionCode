#define gc getchar_unlocked
#else
#define gc _getchar_nolock
#endif

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;

using b32 = bitset<32>;
using b64 = bitset<64>;

inline int readint(){
    int k=0,f=1,c=gc();
    for(;!isdigit(c);c=gc())if(c=='-')f=-1;
    for(;isdigit(c);c=gc())k=10*k+(c&15);
    return k*f;
}

template<class T>
ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline ll exgcd(ll a,ll b,ll& x,ll& y){
    if(b==0){
        x=1,y=0;
        return a;
    }
    ll d = exgcd(b,a%b,x,y);
    ll tx=x;
    x=y;
    y=tx-(a/b)*y;
    return d;
}

inline void solve(){
    int n=readint();
    vector<int> a(n+1),b(n+1);
    ll M=1;
    for(int i=1;i<=n;++i){
        a[i]=readint(),b[i]=readint();
        M*=a[i];
    }

    i128 ans = 0;
    for(int i=1;i<=n;++i){
        ll Mi=M/a[i];
        ll x,y;
        exgcd(Mi,a[i],x,y);
        x = (x%a[i]+a[i])%a[i];
        ans += (i128)Mi*b[i]%M*x;
        ans%=M;
    }
    ans%=M;
    cout<<(ll)ans<<endl;
	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















