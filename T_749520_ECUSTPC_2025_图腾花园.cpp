ostream& operator<<(ostream& os,const vector<T>& v){
    for(const auto& x:v)os<<x<<" ";
    return os;
}

template<class T>
istream& operator>>(istream& is, vector<T>& v){
    for(auto& x:v)is>>x;
    return is;
}

inline void solve(){
    int n = 0;

    string s = "ECUST";
    for(int i=0;i<=31;++i){
        b32 m(n);
        string t = s;
        for(int j=0;j<5;++j){
            int k = 4-j;
            if(m[k]){
                t[j] += 32;
            }
        }
        cout<<t<<"\n";
        n++;
    }


	return;
}


