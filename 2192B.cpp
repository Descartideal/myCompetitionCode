void solve(){
    int n;cin>>n;
    string s;cin>>s;

    int cnt1=0;
    for(int i=0;i<n;++i){
        if(s[i]=='1')cnt1++;
    }

    int cnt0=n-cnt1;

    if(cnt1&1){
        if(cnt0&1){
            vector<int> a(cnt0);

            int cnt = 0;
            for(int i=0;i<n;++i){
                if(s[i]=='0'){
                    a[cnt] = i+1;
                    cnt++;
                }
            }

            cout<<cnt0<<"\n";
            for(int i=0;i<cnt0;++i){
                cout<<a[i]<<" ";
            }cout<<"\n";
            return;
        }
        cout<<-1<<"\n";
        return;
    }else{
        vector<int> a(cnt1);

        int cnt = 0;
        for(int i=0;i<n;++i){
            if(s[i]=='1'){
                a[cnt] = i+1;
                cnt++;
            }
        }

        cout<<cnt1<<"\n";
        if(cnt1==0)return;
        for(int i=0;i<cnt1;++i){
            cout<<a[i]<<" ";
        }cout<<"\n";
    }


	return;
}


