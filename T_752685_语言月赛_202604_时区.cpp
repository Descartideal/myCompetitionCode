inline void solve(){
    int a,b,m,c,d,k;cin>>a>>b>>m>>c>>d>>k;

    int limit = c*60+d;
    int start = a*60+b;
    int end = start+m;

    if(limit<start){
        limit += 1440;
    }

    auto g = [&](int x){
        x = ((x%1440)+1440)%1440;
        int h = x/60;
        int m = x%60;
        cout<<h<<" "<<m<<endl;
    };

    auto f = [&](int x){
        if(x>limit){
            g(x-60);
            return;
        }
        g(x);
    };

    
    int x = start+k;
    while(x<=end){
        
        f(x);x += k;
    }

	return;
}


