inline void solve(){
    int n,m;cin>>n>>m;
    if(m>=2*n){
        cout<<"NO\n";
        return;
    }
    vector<vector<int>> a(2*n+1,vector<int>(m+1,0));
    int i=1,j=1;
    int limit = 2*m;
    int cnt = 0;
    int color = 1;
    while(1){
        a[i][j] = color;
        i++;j++;
        if(i>2*n)i=1;
        if(j>m)j=1;
        while(a[i][j]!=0){cerr<<7<<i;
            i++;
            if(i>2*n)i=1;
        }
        cnt++;
        if(cnt==limit){
            color++;
            cnt=0;
        }
        if(color==n){
            break;
        }
    }
    cout<<"YES\n";
    for(int i=1;i<=2*n;++i){
        for(int j=1;j<=m;++j){
            if(a[i][j]==0)a[i][j]=n;
            cout<<a[i][j]<<" ";
        }cout<<endl;
    }

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t--){
		solve();
	}
	return 0;
}


















