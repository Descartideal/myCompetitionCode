#include <bits/stdc++.h>
using namespace std;
#define int long long

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;


void solve(){
    int n;cin>>n;vector<int> color(n+1),ans(n+1,0),fa(n+1,-1),hson(n+1,0),siz(n+1,0),dep(n+1,0);
    vector<vector<int>> g(n+1);
    for(int i=1;i<=n;++i)cin>>color[i];
    for(int i=1;i<=n-1;++i){
        int u,v;cin>>u>>v;
        g[u].push_back(v);g[v].push_back(u);
    }

    function<int(int,int,int)> dfs1;
    dfs1 = [&](int x,int f,int deep){
        fa[x] = f;
        dep[x] = deep;
        int cnt_siz = 1;
        int max_siz = 0;
        for(int y:g[x]){
            if(y==f)continue;
            int son_siz=dfs1(y,x,deep+1);
            if(son_siz>max_siz){
                hson[x] = y;
                max_siz = son_siz;
            }
            cnt_siz+=son_siz;
        }
        siz[x] = cnt_siz;
        return cnt_siz;
    };
    dfs1(1,0,1);

    vector<int> cnt(n+1);int mx=0,sum=0;
    function<int(int,int)> add;
    add = [&](int x,int son){
        cnt[ color[x] ]++;
        if(cnt[color[x]] > mx){
            sum = color[x];
            mx = cnt[color[x]];
        }else if(cnt[color[x]] == mx){
            sum += color[x];
        }

        for(int y:g[x]){
            if(y==son || y==fa[x])continue;
            add(y,son);
        }
        return 0;
    };
    function<void(int)> sub;
    sub = [&](int x){
        cnt[color[x]]--;
        for(int y:g[x]){
            if(y!=fa[x])sub(y);
        }
    };

    
    function<void(int,int)> dfs2;
    dfs2 = [&](int x,int opt){
        for(int y:g[x]){
            if(y==fa[x] || y==hson[x])continue;
            dfs2(y,0);
        }
        if(hson[x])dfs2(hson[x],1);
        add(x,hson[x]);
        ans[x] = sum;
        if(!opt)sub(x),mx=sum=0;;
    };

    dfs2(1,0);

    for(int i=1;i<=n;++i){
        if(i-1)cout<<" ";
        cout<<ans[i];
    }

	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	solve();
	return 0;
}


















