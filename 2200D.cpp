#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;

using b32 = bitset<32>;
using b64 = bitset<64>;

inline void solve(){
    int n,l,r;cin>>n>>l>>r;
    vector<int> a(n+1);
    vector<int> out,in;
    for(int i=1;i<=n;++i){
        cin>>a[i];
        if(l<i && i<=r){
            in.push_back(a[i]);
        }else{
            out.push_back(a[i]);
        }
    }

    int pin = min_element(in.begin(),in.end())-in.begin();

    vector<int> ai,ao;
    for(int i=0;i<(int)in.size();++i){
        ai.push_back( in[(i+pin)%in.size()] );
    }
    for(int i=0;i<(int)out.size();++i){
        ao.push_back( out[i] );
    }

    int pout=(int)ao.size();
    for(int i=0;i<(int)ao.size();++i){
        if(ao[i]>ai[0]){
            pout=i;
            break;
        }
    }

    vector<int> ans;
    if(ao.size()==0){
        for(int i=0;i<(int)ai.size();++i){
            ans.push_back(ai[i]);
        }
    }else{
        for(int i=0;i<(int)ao.size();++i){
            if(i==pout){
                for(int j=0;j<(int)ai.size();++j){
                    ans.push_back(ai[j]);
                }
            }
            ans.push_back(ao[i]);
        }
        if(pout==(int)ao.size()){
            for(int j=0;j<(int)ai.size();++j){
                ans.push_back(ai[j]);
            }
        }
    }

    for(int i=0;i<n;++i){
        cout<<ans[i]<<" ";
    }cout<<endl;

    return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















