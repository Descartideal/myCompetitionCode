void solve(){
	int n;cin>>n;
	vector<int> a(n+1);
	for(int i=1;i<=n;++i){cin>>a[i];}
	if(n==1){
		cout<<a[1];return;
	}
	
	vector<int> b(n+1,0);
	b[1]=a[1];
	for(int i=2;i<=n;++i){
		b[i]=max(b[i-1],a[i]+b[i-2]);
	}
	cout<<b[n];
	return;
}


