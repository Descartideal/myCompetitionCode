inline void solve(){
    vector<int> dx={-1,0,1,0},dy={0,1,0,-1};
    int n,m,k;cin>>n>>m>>k;k++;

    if(k<n+m-1){
        cout<<"NO"<<endl;
        return;
    }

    int x = k-n-m+1;
    if(x&1){
        cout<<"NO"<<endl;
        return;
    }

    vector<vector<int>> hen(n+1,vector<int>(m,-1));
    vector<vector<int>> shu(n,vector<int>(m+1,-1));

    /** 
    int y = x/2;
    for(int i=1;i<=n-1;++i){
        shu[1][i] = 2;
    }
    for(int i=1;i<=m-1;++i){
        hen[n][i] = 1;
    }
    **/

    vector<vector<int>> a(n+1,vector<int>(m+1,-1));
    for(int i=1;i<=n-1;++i){
        a[i][1] = 2;
    }
    for(int i=1;i<=m-1;++i){
        a[n][i] = 1;
    }

    int cntx = (n-1)/2 , cnty = m-1;
    int cntz;
    if(n%2==0){
        cntz = (m-1)/2;
    }

    //int y = x/2;
    int y;
    if(x==2){
        y=1;
    }else{
        y=0;
    }
    int p1 = cntx*cnty;
    for(int i=1;i<=y;++i){
        if(i<=p1){
            int px = (i-1)/cnty+1;
            int py = (i-1)%cnty+1;
            int rx = px;
            int ry = py*2;

            a[rx][ry-1] = 1;

            a[rx][ry] = 2;
            a[rx+1][ry] =3;
        }else{
            int p = i-p1;
            int rx = n-1;
            int ry = 2*p;

            a[rx+1][ry] = 0;

            a[rx][ry] = 1;
            a[rx][ry+1] = 2;
        }
    }
    /**
    for(int i=1;i<=n;++i){
        for(int j=1;j<=m;++j){
            cout<<a[i][j]<<"    ";
        }cout<<endl;
    }
    **/

    int px=1,py=1;
    int s = 0;
    while(px!=n || py!=m){
        int t = a[px][py];
        if(t==1 || t==2){
            if(t==1){
                hen[px][py] = s;
            }else{
                shu[px][py] = s;
            }
        }else{
            if(t==0){
                shu[px-1][py] = s;
            }else{
                hen[px][py-1] = s;
            }
        }
        s = 1-s;
        px += dx[t];
        py += dy[t];
    }

    if(x!=2){
        hen[1][1] = 1;
        hen[1][2] = 0;
        
        hen[2][1] = 1;
        hen[2][2] = 0;

        shu[1][2] = 0;
        shu[1][3] = 1;
    }

    cout<<"YES"<<endl;

    for(int i=1;i<=n;++i){
        for(int j=1;j<=m-1;++j){
            if(hen[i][j]==0){
                cout<<"B ";
            }else{
                cout<<"R ";
            }
        }cout<<endl;
    }

    for(int i=1;i<=n-1;++i){
        for(int j=1;j<=m;++j){
            if(shu[i][j]==0){
                cout<<"B ";
            }else{
                cout<<"R ";
            }
        }cout<<endl;
    }


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















