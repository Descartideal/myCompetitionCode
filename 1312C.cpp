#include <bits/stdc++.h>
using namespace std;

using pii = pair<int,int>;

using i64 = long long;
using ll = long long;
using u64 = unsigned long long;
using u32 = unsigned;

using u128 = unsigned __int128;
using i128 = __int128;

using b32 = bitset<32>;
using b64 = bitset<64>;

inline void solve(){
    ll n,k;cin>>n>>k;
    vector<ll> a(n);for(ll &x:a)cin>>x;
    ll s = accumulate(a.begin(),a.end(),0LL);

    if(s==0){
        cout<<"YES"<<endl;
        return;
    }

    vector<ll> p;

    p.push_back(1);
    for(int i=1;;++i){
        ll x = p.back()*k;
        if(x>0 && x<1e18){
            p.push_back(x);
        }else{
            break;
        }
    }

    ll sp = 0;
    ll pos = 0;
    for(int i=0;i<p.size();++i){
        sp += p[i];
        if(sp>=s){
            pos = i;
            break;
        }
    }
    sort(a.begin(),a.end());


    vector<bool> w(pos+1,0);
    for(int i=n-1;i>=0;--i){
        for(int j=pos;j>=0;--j){
            if(p[j]<=a[i] && w[j]==0){
                a[i] -= p[j];
                w[j] = 1;
            }
        }
        if(a[i]>0){
            cout<<"NO"<<endl;
            return;
        }
    }
    cout<<"YES"<<endl;


	return;
}


signed main(){
	ios::sync_with_stdio(false);
	cin.tie(nullptr);cout.tie(nullptr);cerr.tie(nullptr);
	int t;cin>>t;
	while(t-->0){
		solve();
	}
	return 0;
}


















